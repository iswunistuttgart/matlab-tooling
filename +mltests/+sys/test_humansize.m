function test_humansize()

% A random test size
dTestSize = 34.54637281;

% The expected values, determined manually and beforehand following the pattern
% of dTestSize*(1024^(test-1)) and rounded to test-1 decimals
ceExpectedValues = {...
    '35 B', ...
    '34.5 kB', ...
    '34.55 MB', ...
    '34.546 GB', ...
    '34.5464 TB', ...
    '34.54637 PB', ...
    '34.546373 EB', ...
    '34.5463728 ZB', ...
    '34.54637281 YB', ...
    '35375.485757440 YB', ...
    '36224497.4156185610 YB', ...
};

% Test for all these values above
for iTest = 1:numel(ceExpectedValues)
    assert(strcmp(humansize(dTestSize*(1024^(iTest-1)), iTest-1), ceExpectedValues{iTest}), 'Failed for test %i', iTest);
end


end

